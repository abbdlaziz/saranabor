          <h5><p>PT. SARANABOR BUMI PERKASA</p></h5>
          <marquee direction="up" style="background:url(img/about.jpg)" scrollamount="1"><font color="#F8F3F3"><p>PT. SARANABOR BUMI PERKASA berdiri sebagai kelanjutan dari CV SARANABOR & CHEMINDO berdiri sejak 18 Desember 2002.  Mulai 13 Desember 2011 dilakukan pengembangan usaha aksesories alat bor untuk eksplorasi bahan tambang; mineral dan batubara; seismic serta geoteck dengan nama produk  ‘Bill Smith’.</p>
          <p>Pendiri perusahaan berdedikasi Teknik Pertambangan, sadar betul dengan kekayaan SDA di Indonesia berniat supaya dapat mensejahterakan rakyat Indonesia. Tujuan mendirikan perusahaan ini juga memberikan kemudahan penyediaan aksesories alat bor yang bermutu dan memenuhi standar DCDMA (Diamond Core Drilling Manufactures Association) untuk eksplorasi bahan tambang di wilayah Indonesia, dimana aksesories tersebut sebelumnya sangat susah diperoleh yang harus import dari negara luar.</p>
          <p>Dengan keberadaan PT. Saranabor Bumi Perkasa, telah banyak perusahaan pengeboran dan pertambangan dibantu. Baik di wilayah Indonesia ataupun di luar negeri terutama di Malaysia. Peralatan mudah didapat dan segera secepatnya diterima di lapangan. Demikian juga harga kompetitive dibandingkan dengan barang impor. Mutu produk tetap kami utamakan  berkwalitas ekspor.</p>
          <p>PT. Saranabor Bumi Perkasa murni perusahaan nasional. Tenaga kerja 100% bangsa Indonesia terlatih. Saat ini lokasi pabrik menempati lahan seluas 1200 m² yang berdekatan dengan gudang seluas 1300 m2 di daerah Jatiasih-Bekasi. Untuk mempertahankan kwalitas produk, pengontrolan  pekerja  terampil serta  fasilitas pendukung dengan unit produksi mesin-mesin CNC.</p>
          <p>Produk kami sudah banyak dipakai perusahaan BUMN, Swasta Nasional serta ekspor. Dalam rangka memenuhi kebutuhan customer kami jug membantu menerima design/rancangan khusus untuk aksesories bor yang dikehendaki oleh pemakai (end user).</p>
          <p>Optimis dengan tujuan baik PT. SARANABOR BUMI PERKASA akan banyak memberikan kebaikan bagi Indonesia serta semua orang khususnya yang bergelut di dunia pertambangan. </p></marquee></font>
